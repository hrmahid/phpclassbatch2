<?php
	//while loop
	$x = 1;

	while($x <= 5) {
		echo "The number is: $x <br>";
		$x++;
	}
	//do while loop 
	echo "<br>";
	
	$a = 1;

	do {
		echo "The number is: $a <br>";
		$a++;
	}while ($a == 0);
	
	echo "<br>";
	//for loops
	for($x = 0; $x <= 10; $x++) {
		echo "The number is: $x <br>";
	}
	
	
	
	
	
	
	
	
	
	
?>